	public static final String DELIMITER = ":";
	public static final String ARGS_SEPARATOR = "\"";
	private static final String PREFIX = "$[";
	private static final String POSTFIX = "]";
	private static final String ESCAPE = ".";
	
	private static Log m_log = LogFactory.getLog( MessageConstant.class );
	
	public String getMessage(String... args) {
		return getMessageId(toString(), args);
	}
	
	public static String getMessageId(String messageId, String... args) {
		StringBuilder builder = new StringBuilder();
		builder.append(PREFIX + messageId);
		if(m_log.isTraceEnabled()){
			m_log.trace("before = " + builder.toString());
		}
		for (String s : args) {
			builder.append(DELIMITER + ARGS_SEPARATOR + s + ARGS_SEPARATOR);
		}
		if(m_log.isTraceEnabled()){
			m_log.trace("after = " + builder.toString());
		}
		return builder.toString() + POSTFIX;
	}
	
	public static String escape (String s) {
		return s.replaceAll("$\\[", ESCAPE).replaceAll("\\]", ESCAPE).replaceAll("\\:", ESCAPE);
	}
	
	public static void main (String args[]) {
		String s = MessageConstant.ADMINISTRATOR.getMessage();
		System.out.println("s=" + s);
		System.out.println("s=" + escape(s));
	}
