package com.clustercontrol.rest.endpoint.access.dto;

import com.clustercontrol.fault.InvalidSetting;
import com.clustercontrol.rest.annotation.RestItemName;
import com.clustercontrol.rest.annotation.validation.RestValidateString;
import com.clustercontrol.rest.dto.RequestDto;
import com.clustercontrol.util.MessageConstant;

public class ChangePasswordRequest implements RequestDto {

	public ChangePasswordRequest() {
	}

	@RestItemName(value = MessageConstant.PASSWORD)
	@RestValidateString(notNull = true, maxLen = 64)
	private String password;

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	@Override
	public String toString() {
		return "ChangePasswordRequest [password=" + password + "]";
	}

	@Override
	public void correlationCheck() throws InvalidSetting {
	}

}
