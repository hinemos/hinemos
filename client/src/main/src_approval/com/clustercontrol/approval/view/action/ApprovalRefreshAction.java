package com.clustercontrol.approval.view.action;

import org.apache.commons.logging.Log;
import org.apache.commons.logging.LogFactory;
import org.eclipse.core.commands.AbstractHandler;
import org.eclipse.core.commands.ExecutionEvent;
import org.eclipse.core.commands.ExecutionException;
import org.eclipse.ui.IWorkbenchPart;
import org.eclipse.ui.handlers.HandlerUtil;

import com.clustercontrol.approval.view.ApprovalView;

public class ApprovalRefreshAction extends AbstractHandler {
	/** ログ */
	private static Log m_log = LogFactory.getLog(ApprovalRefreshAction.class);

	/** アクションID */
	public static final String ID = ApprovalRefreshAction.class.getName();

	/** ビュー */
	private IWorkbenchPart viewPart;

	/**
	 * Dispose
	 */
	@Override
	public void dispose() {
		this.viewPart = null;
	}

	@Override
	public Object execute(ExecutionEvent event) throws ExecutionException {
		// 選択アイテムの取得
		this.viewPart = HandlerUtil.getActivePart(event);
		ApprovalView view = null;
		try {
			view = (ApprovalView) this.viewPart.getAdapter(ApprovalView.class);
		} catch (Exception e) { 
			m_log.info("execute " + e.getMessage()); 
			return null; 
		}

		if (view == null) {
			m_log.info("execute: view is null"); 
			return null;
		}

		// ビューの更新
		view.update();
		return null;
	}

}
